      <div>
        <h2>Tarte aux pommes</h2>
        <p>Temps de préparation : 1heure (30minutes de cuissons)</p>
        <p><strong>Ingrédients (pour 6 personnes) :</strong></p>

        <ul>
          <li> 1 pâte brisée</li>
          <li> 6 pommes Golden</li>
          <li> 1 sachet de sucre vanillé</li>
          <li> 30 g de beurre</li>
        </ul>
      </div>
    </a>
    <div class="preparation">
      <p>Éplucher et découper en morceaux 4 Golden.</p>

      <p>Faire une compote : les mettre dans une casserole avec un peu d'eau (1 verre ou 2). Bien remuer.
        Quand les pommes commencent à ramollir, ajouter un sachet ou un sachet et demi de sucre vanillé.
        Ajouter un peu d'eau si nécessaire.
      Vous saurez si la compote est prête une fois que les pommes ne seront plus dures du tout.
      Ce n'est pas grave s'il reste quelques morceaux.</p>

      <p>Pendant que la compote cuit, éplucher et couper en quatre les deux dernières pommes, puis, couper les
        quartiers en fines lamelles (elles serviront à être posées sur la compote).</p>

      <p>Préchauffer le four à 210°C (thermostat 7).</p>
      <p>Laisser un peu refroidir la compote et étaler la pâte brisée dans un moule et la piquer avec une fourchette. </p>

      <p>Verser la compote sur la pâte et placer les lamelles de pommes en formant une spirale ou plusieurs cercles, au choix !
        Disposer des lamelles de beurre dessus.</p>

      <p>Mettre au four et laisser cuire pendant 30 min max. Surveiller la cuisson.
        Vous pouvez ajouter un peu de sucre vanillé sur la tarte pendant que ça cuit pour caraméliser un peu.</p>
